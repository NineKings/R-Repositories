#while 循环
i = 1
sum = 0
while(i <= 100){
sum = sum + i
i = i + 1
}
print(sum)

#用while循环实现基数项总和
i = 1
sum = 0
while(i <= 100){
sum = sum + i
i = i + 2
}
print(sum)

#同while循环实现偶数项总和
i = 2
sum = 0
while(i <= 100){
sum = sum + i
i = i + 2
}
print(sum)

#for循环1到100的和
sum=0
for(i in 1:100){
if(i<=100)
sum=sum+i
}
print(sum)


